package Homework.Homework1;

public class IfForExample {
    public static void main(String[] args) {
        //Առաջին առաջադևանք
        int a = 15, b = 24;
        if (a > b) {
            System.out.println(a);
        } else {
            System.out.println(b);
        }

        //Երկրորդ առաջադրանք
        for (a = 15; a <= 24; a++) {
            System.out.println(a);
        }

        //char
        char ch1, ch2;
        ch1 = 15;
        ch2 = 24;
        System.out.println(ch1 + "" + ch2);

        // boolean
        System.out.println(a == b);
    }

}









